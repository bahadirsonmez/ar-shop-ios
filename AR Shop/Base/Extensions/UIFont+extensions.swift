import UIKit.UIFont

extension UIFont {
    static let thin14: UIFont = UIFont.systemFont(ofSize: 14, weight: .thin)
    
    static let medium14: UIFont = UIFont.systemFont(ofSize: 14, weight: .medium)
    static let medium16: UIFont = UIFont.systemFont(ofSize: 16, weight: .medium)
    static let medium18: UIFont = UIFont.systemFont(ofSize: 18, weight: .medium)
    
    static let regular14: UIFont = UIFont.systemFont(ofSize: 14, weight: .regular)
    static let regular18: UIFont = UIFont.systemFont(ofSize: 18, weight: .regular)

    static let semiBold18: UIFont = UIFont.systemFont(ofSize: 18, weight: .semibold)
    
    static let bold36: UIFont = UIFont.systemFont(ofSize: 36, weight: .bold)

}
